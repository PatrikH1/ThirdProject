package jonas;

public class TypesInJava {

	public static void main(String[] args) {
		int zero = 0;
		String hello = "hello";
		int oneZeroOne = 101;
		int minusOne = -1;
		boolean valueTrue = true;
		String threeThree = "33";
		double pi = 3.1416;
        
		System.out.println("All types:");
		System.out.println("int zero: " + zero);
		System.out.println("String hello: " + hello);
		System.out.println("int oneZeroOne: " + oneZeroOne);
		System.out.println("int minusOne: " + minusOne);
		System.out.println("boolean valueTrue: " + valueTrue);
		System.out.println("String threeThree: " + threeThree);
		System.out.println("double pi: " + pi);
	}

}
