package Vector;

import java.util.Objects;

class InfoIntegerLong {
    private Integer year;
    private Long value;

    public InfoIntegerLong(Integer year, Long value) {
        this.year = year;
        this.value = value;
    }

    public Integer getYear() {
        return year;
    }

    public Long getValue() {
        return value;
    }
}

